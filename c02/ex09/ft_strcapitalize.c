char	*ft_strcapitalize(char *str)
{
	int	i;
	int	b;

	i = 0;
	b = 1;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z')
			|| (str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] >= '0' && str[i] <= '9'))
		{
			if (b && (str[i] >= 'a' && str[i] <= 'z'))
				str[i] -= 32;
			else if (b != 1 && (str[i] >= 'A' && str[i] <= 'Z'))
				str[i] += 32;
			b = 0 ;
		}
		else
		{
			b = 1;
		}
		i++;
	}
	return (str);
}
