int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	if (str[0] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (str[i] >= 'A')
		{
			if (str[i] >= 'Z')
				if (!(str[i] >= 'a' || str[i] <= 'z'))
					return (0);
		}
		else
			return (0);
		i++;
	}
	return (1);
}
