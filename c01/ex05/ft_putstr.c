#include <unistd.h>
#include <stdio.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int main()
{
	char str[] = "hello anas";
	ft_putstr(str);
	printf("\n%d\n", str);
	printf("%d\n", &str[0]);
	printf("%c\n", str[0]);
}
