int	ft_is_prime(int nb)
{
	int	i;
	int	c_p;

	if (nb == 0 || nb == 1)
		return (0);
	if (nb < 0)
		return (0);
	i = 2;
	c_p = 0;
	while (i < nb)
	{
		if (nb % i == 0)
			c_p++;
		i++;
	}
	if (c_p == 0)
		return (1);
	return (0);
}
