#include <unistd.h>

void	ft_print_comb(void)
{
	int	i;
	int	ii;
	int	iii;

	i = '0';
	while (i <= '7')
	{
		ii = i + 1;
		while (ii <= '8')
		{
			iii = 1 + ii;
			while (iii <= '9')
			{
				write(1, &i, 1);
				write(1, &ii, 1);
				write(1, &iii, 1);
				if (!(i == '7' && ii == '8' && iii == '9'))
					write(1, ", ", 2);
				iii++;
			}
			ii++;
		}
		i++;
	}
}
