#include <unistd.h>

void	ft_putnbr(int nb)
{
	unsigned int	nr;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	nr = nb;
	if (nr > 9)
	{
		ft_putnbr(nr / 10);
		ft_putnbr(nr % 10);
	}
	if (nr <= 9 && nr >= 0)
	{
		nr += '0';
		write(1, &nr, 1);
	}
}
